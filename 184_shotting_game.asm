ORG 00H
START:
MOV 2EH, #00000001B
CLR 0x46 ; level ONE bit condition setting
CLR 0x47 ; level TWO bit condition setting
ACALL RESET_BITS
CLR 0x45 ; bit checker for checking toogle button
LJMP SELECT_DIFFICULTY_ONE
START_GAME_ANIM:
MOV B, #8
MOV 2EH, #00000001B
ACALL START_FX
MOV B, #0
; clear all bit checkers for new game start
ACALL RESET_BITS
CLR 0x45
MOV 2EH, #00000001B
MOV R6, #0
MOV P1, #0
; pointers for each row for code logic and functions
MOV R0, #25H ; for shooting and destruction functions
MOV R1, #20H ; for display loop function
; initialize positions at the start of the game
MOV 20H, #00000000B
MOV 21H, #00000000B
MOV 22H, #00000000B
MOV 23H, #11111111B
MOV 24H, #11111111B
MOV 25H, #11111111B
MOV 26H, #11010111B
MOV 27H, #11000111B

MOV 2CH, #11110111B ; starting position of RIGHT gun
MOV 2DH, #11011111B ; starting position of LEFT gun

LED_MATRIX_LOOP: ; main loop funtion
		MOV 2FH, #8 ; number of loops, 8 loops refresh the entire led matrix
			    ; increasing the value of 2FH by multiples of 8 will result in slower frame rate
		ACALL REFRESH_DISP  ; call display loop to refresh the display
		ACALL CHECK_ACTION   ; call input checker
		ACALL ACTION_ONCE    ; check if button is toggled
		JNB 0x42, SKIP13     ; 0x42 is NOT set
		JNB 0x46, SKIP30     ; check if level ONE for shooting mechanics
		ACALL DESTROYED_LINE ; destroy whole column of enemies
	SKIP30:
		JNB 0x47, SKIP13    ; check if level TWO for shooting mechanics
		ACALL DESTROYED_ONE_DOT ; destroy only one enemy
	SKIP13:
		JNB 0x40, SKIP9  ; skips shoot function if gun is not shot (for left gun)
		ACALL SHOOT	 ; display bullet traveling
		SETB 0x42	 ; set bit to know if a bullet is traveling
	SKIP9:
		JNB 0x41, SKIP11 ; skips shoot function if gun is not shot (for right gun)
		ACALL SHOOT	 ; display bullet traveling
		SETB 0x42	 ; set bit to know if a bullet is traveling
	SKIP11:
		CLR 0x43
		CLR 0x44
;	======= ADD TOP LAYER 1 SPEED =======
	ADD_LAYER_1:
		JB 0x48, ADD_LAYER_2
		JB 0x42, TO_SKIP3 ; check if a bullet is still travelling, if yes do not add new layer
			       ; this condition is implemented because of an instance where a bullet, positioned
			       ; at the row above the player, is considered an enemy while a layer is being added
			       ; resulting in the lost of the game
		INC R6
		JNB 0x46, SKIP32    ; check if level ONE to add layer
 		CJNE R6, #45, TO_SKIP3 ; R6 is the number of frame updates before adding a layer of enemies
	SKIP32:			    ; the bigger the value of R6 the longer it takes for enemies to spawn
		JNB 0x47, SKIP33    ; check if level TWO to add layer
		CJNE R6, #75, TO_SKIP3
	SKIP33:
		MOV R6, #0
		ACALL ADD_LAYER_FUNC
		CJNE R7, #8, TO_SKIP3
		MOV R7, #0
		SETB 0x48
		JMP SKIP3
;	======= ADD TOP LAYER 2 SPEED =======
	ADD_LAYER_2:
		JB 0x49, ADD_LAYER_3
		JB 0x42, SKIP3
		INC R6
		JNB 0x46, SKIP38
 		CJNE R6, #40, SKIP3
	SKIP38:
		JNB 0x47, SKIP39
		CJNE R6, #70, SKIP3
	SKIP39:
		MOV R6, #0
		ACALL ADD_LAYER_FUNC
		CJNE R7, #10, SKIP3
		MOV R7, #0
		SETB 0x49
	TO_SKIP3:
		JMP SKIP3
;	======= ADD TOP LAYER 3 SPEED =======
	ADD_LAYER_3:
		JB 0x4A, ADD_LAYER_4
		JB 0x42, SKIP3
		INC R6
		JNB 0x46, SKIP40
 		CJNE R6, #35, SKIP3
	SKIP40:
		JNB 0x47, SKIP41
		CJNE R6, #65, SKIP3
	SKIP41:
		MOV R6, #0
		ACALL ADD_LAYER_FUNC
		CJNE R7, #12, SKIP3
		MOV R7, #0
		SETB 0x4A
		JMP SKIP3
;	======= ADD TOP LAYER 4 SPEED =======
	ADD_LAYER_4:
		JB 0x4B, ADD_LAYER_5
		JB 0x42, SKIP3
		INC R6
		JNB 0x46, SKIP42
 		CJNE R6, #30, SKIP3
	SKIP42:
		JNB 0x47, SKIP43
		CJNE R6, #60, SKIP3
	SKIP43:
		MOV R6, #0
		ACALL ADD_LAYER_FUNC
		CJNE R7, #14, SKIP3
		MOV R7, #0
		SETB 0x4B
		JMP SKIP3
;	======= ADD TOP LAYER 5 SPEED =======
	ADD_LAYER_5:
		JB 0x42, SKIP3
		INC R6
		JNB 0x46, SKIP44
 		CJNE R6, #25, SKIP3
	SKIP44:
		JNB 0x47, SKIP45
		CJNE R6, #55, SKIP3
	SKIP45:
		MOV R6, #0
		ACALL ADD_LAYER_FUNC
	SKIP3:
		JMP LED_MATRIX_LOOP

ACTION_ONCE: ; checks if button is toggled, if toggled the function is only executed once
	JNB P3.0, SKIP12
	JNB P3.1, SKIP12
	JNB P3.2, SKIP12
	JNB P3.3, SKIP12
	CLR 0x45 ; clears 0x45 to accept new input
	SKIP12:
	RET

CHECK_ACTION:	; call specific functions for each inputs
	JNB 0x43, SKIP16
	ACALL MOVE_LEFT  ; for left movement
	SKIP16:
	JNB 0x44, SKIP17
	ACALL MOVE_RIGHT ; for right movement
	SKIP17:
	JB 0x42, SKIP18  ; 0x42 is the bit condition seeting to avoid firing multiple shots/guns
	JNB 0x40, SKIP18 ; condition to fire only one gun at a time
	ACALL LEFT_GUN   ; for shooting left gun
	SKIP18:
	JB 0x42, SKIP19  ; 0x42 is the bit condition seeting to avoid firing multiple shots/guns
	JNB 0x41, SKIP19 ; condition to fire only one gun at a time
	ACALL RIGHT_GUN  ; for shooting right gun
	SKIP19:
	RET

CHECK_INPUT:	; checks for user inputs      , JB commands checks if bit conditions are set
		; sets its corresponding bit for condition checkers
		JB 0x45, DONT_SHOOT_LEFT_SKIP ; 0x45 is the toggle bit condition setting
		JB 0x44, DONT_MOVE_LEFT       ; 0x44 is the right move bit condition setting
		JB P3.0, DONT_MOVE_LEFT
		SETB 0x43
		SETB 0x45
		JMP DONT_MOVE_RIGHT
	DONT_MOVE_LEFT:
		JB 0x43, DONT_MOVE_RIGHT ; 0x43 is the left move bit condition setting
		JB P3.1, DONT_MOVE_RIGHT
		SETB 0x44
		SETB 0x45
	DONT_MOVE_RIGHT:
		JB 0x42, DONT_SHOOT_LEFT_SKIP ; 0x42 is the bit condition seeting to avoid firing multiple shots/guns
		JB 0x40, DONT_SHOOT_RIGHT ; 0x41 is the LEFT gun bit condition setting
		JB P3.2, DONT_SHOOT_RIGHT
		SETB 0x41
		SETB 0x45
		JMP DONT_SHOOT_LEFT_SKIP
	DONT_SHOOT_RIGHT:
		JB 0x42, DONT_SHOOT_LEFT_SKIP ; 0x42 is the bit condition seeting to avoid firing multiple shots/guns
		JB 0x41, DONT_SHOOT_LEFT_SKIP ; 0x41 is the RIGHT gun bit condition setting
		JB P3.3, DONT_SHOOT_LEFT_SKIP
		SETB 0x40
		SETB 0x45
	DONT_SHOOT_LEFT_SKIP:
	RET

ADD_LAYER_FUNC: ; adds a layer of enemies at the top
	MOV A, 25H
	CPL A
	JNZ LONG_JUMP_LOSER_FX ; checks if enemies reach the bottom
	MOV 25H, 24H
	MOV 24H, 23H
	MOV 23H, 22H
	MOV 22H, 21H
	MOV 21H, 20H
	MOV 20H, #00000000B
	INC R7
	RET
LONG_JUMP_LOSER_FX:
	MOV B, #8
	ACALL RESET_BITS
	CLR 0x45
	LJMP LOSER_FX

REFRESH_DISP: ; refresh whole display
	ACALL CHECK_INPUT
	MOV P0, @R1
	ACALL GEN_DISP
	INC R1
	CJNE R1, #28H, SKIP1
		MOV R1, #20H
	SKIP1:
	DJNZ 2FH, REFRESH_DISP
	RET

RIGHT_GUN: ; initialize right gun for shooting
	MOV B, 2CH
	RET

LEFT_GUN: ; initialize left gun for shooting
	MOV B, 2DH
	RET

MOVE_RIGHT: ; move player to the right
	MOV A, 27H
	CJNE A, #11111000B, MOVE1 ; checks if player is at the corner of the display
	RET
	MOVE1:
	MOV A, 26H
	RR A
	MOV 26H, A
	MOV A, 2DH
	RR A
	MOV 2DH, A
	MOV A, 2CH
	RR A
	MOV 2CH, A
	MOV A, 27H
	RR A
	MOV 27H, A
	RET

MOVE_LEFT: ; move player to the left
	MOV A, 27H
	CJNE A, #00011111B, MOVE2 ; checks if player is at the corner of the display
	RET
	MOVE2:
	MOV A, 26H
	RL A
	MOV 26H, A
	MOV A, 2DH
	RL A
	MOV 2DH, A
	MOV A, 2CH
	RL A
	MOV 2CH, A
	MOV A, 27H
	RL A
	MOV 27H, A
	RET

SHOOT: ; displays the bullet
	MOV A, @R0
	MOV R4, A ; store current byte in R4 for DESTROYED_ONE_DOT function
	ANL A, B
	MOV @R0, A
	CJNE R0, #1FH, SKIP14
	MOV R0, #25H
	SKIP14:
	RET

DESTROYED_LINE: ; destroy all enemies in the column
	MOV A, @R0
	XRL A, B
	CPL A
	MOV @R0, A
	DEC R0
	CJNE R0, #1FH, SKIP15
	MOV R0, #25H
	CLR 0x42
	CLR 0x41
	CLR 0x40
	SKIP15:
	RET

DESTROYED_ONE_DOT: ; destroy only one dot of enemy
	MOV A, @R0
	XRL A, B
	CPL A
	MOV @R0, A
	SUBB A, R4 ; checks if R4 and the XNORed previous row is the same
	JZ SKIP21  ; if the same stop deleting dots in the column, destroys only one dot
	MOV R0, #20H
	SKIP21:
	DEC R0
	CJNE R0, #1FH, SKIP31
	MOV R0, #25H
	CLR 0x42
	CLR 0x41
	CLR 0x40
	SKIP31:
	RET

RESET_BITS:
	CLR 0x40 ; bit checker for LEFT GUN
	CLR 0x41 ; bit checker for RIGHT GUN
	CLR 0x42 ; bit checker for bullet traveling
	CLR 0x43 ; bit checker for LEFT MOVE
	CLR 0x44 ; bit checker for RIGHT MOVE
	CLR 0x48 ; bit checker for 1 speed enemy spawn
	CLR 0x49 ; bit checker for 2 speed enemy spawn
	CLR 0x4A ; bit checker for 3 speed enemy spawn
	CLR 0x4B ; bit checker for 4 speed enemy spawn
	RET

GEN_DISP: ; general display loop
	MOV A, 2EH
	MOV P1, A
	ACALL DELAY
	MOV P1, #0
	RL A
	MOV 2EH, A
	RET

CHECK_RESTART: ; check for inputs to restart game after losing
	JB 0x43, SKIP20
	JB 0x40, SKIP20
	RET
	SKIP20:
	LJMP START

GEN_DISP_W_INPUT_CHECKER: ; general display loop with level select checker\
	JB 0x45, SKIP27
	JB P3.0, SKIP22
	SETB 0x45
	SETB 0x43
	SKIP22:
	JB P3.1, SKIP23
	SETB 0x45
	SETB 0x43
	SKIP23:
	JB P3.2, SKIP26
	SETB 0x45
	SETB 0x40
	SKIP26:
	JB P3.3, SKIP27
	SETB 0x45
	SETB 0x40
	SKIP27:
	ACALL GEN_DISP
	RET

